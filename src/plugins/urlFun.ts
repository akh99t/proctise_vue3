// 判断网址是否符合格式
export const isValidURL = (url: string) => {
  let pattern = new RegExp(
    "^(https?:\\/\\/)?" + // 协议部分，可选
      "((([a-z\\d]([a-z\\d-]*[a-z\\d])*)\\.)+[a-z]{2,}|" + // 域名部分
      "((\\d{1,3}\\.){3}\\d{1,3}))" + // 或者IP地址
      "(\\:\\d+)?(\\/[-a-z\\d%_.~+]*)*" + // 端口号和路径部分
      "(\\?[;&a-z\\d%_.~+=-]*)?" + // 查询字符串
      "(\\#[-a-z\\d_]*)?$",
    "i"
  ); // 锚点部分

  // 使用正则表达式测试输入的网址是否符合格式
  return pattern.test(url);
};
